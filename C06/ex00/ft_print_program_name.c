#include <unistd.h>

int	main(int ac, char **ag)
{
	int	i;

	i = 0;
	ac = 1;
	while (ag[0][i])
	{
		write(1, &ag[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
